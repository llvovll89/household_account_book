export interface KrStockResult {
  symbol: string
  shortName: string
  quoteType: 'EQUITY'
}

const KR_STOCK_MAP = new Map(KR_STOCKS.map(([sym, name]) => [sym, name]))

/**
 * 심볼로 한국어 이름 조회 (예: '005930.KS' → '삼성전자')
 */
export function getKrStockName(symbol: string): string | null {
  return KR_STOCK_MAP.get(symbol) ?? null
}

/**
 * 한글 종목명으로 정적 리스트를 검색 (부분 일치)
 */
export function searchKrStocks(keyword: string): KrStockResult[] {
  const q = keyword.trim()
  if (!q) return []
  return KR_STOCKS
    .filter(([, name]) => name.includes(q))
    .slice(0, 6)
    .map(([symbol, name]) => ({ symbol, shortName: name, quoteType: 'EQUITY' as const }))
}
